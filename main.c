#include "get_next_line.h"

int		main(void)
{
	int		fd;
	char	*str;
	int		ret;

	ret = 1;
	fd = open("test.txt", O_RDONLY);
	while (ret > 0)
	{
		ret = get_next_line(fd, &str);
		printf("%s\n", str);
	}
	return (0);
}
